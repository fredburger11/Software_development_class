package Software_development_class.source;

public class User{
    private String name;
    private int id;

    // Setters and Getters

    // Name
    public void setName(String userName) {
        name = userName;
    }

    public String getName() {
        return name;
    }

    // Id
    public void setId(int userID) {
        id = userID;
    }

    public int getId() {
        return id;
    }
}
